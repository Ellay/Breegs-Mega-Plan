	/////////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////// GET////////////////////////////////////////////////////////////////////////
	
	function get_file_by_alias($alias){
		$this->db->select('*');
		$this->db->from('mail_attach');
		$this->db->where("mail_file_alias", $alias);
		$query = $this->db->get();
		return $query->row();
	}
	function get_position_list(){
		$this->db->select('*');
		$this->db->from('position');
		$query = $this->db->get();
		return $query->result();
	}
